#include<stdio.h>
void main()
{
    int i,j,k,l,m;
    for(i=1;i<=4;i++)
    {
        i%2==0?(k=2):(k=1);
        for(j=1;j<=k;j++)
        {
            printf("*\n");
        }
        i==3?(m=2):(m=0);
        if(i<4)
        {
            for(l=1;l<=4*i-m;l++)
            {
                printf("*");
            }
            printf("\n");
        }
    }
}